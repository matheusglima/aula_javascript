//Igual (==)
//Retorna verdadeiro caso os operandos sejam iguais
//Ex:
var1 == 3

//Não igual (!=)
//Retorna verdadeiro caso os operandos sejam diferentes
//Ex:
var2 != 3

//Estritamente igual (===)
//Retorna verdadeiro caso os operandos sejam estritamente iguais
//Ex:
var2 === 3

//Estritamente não igual (!==)
//Retorna verdadeiro caso os operandos não sejam estritamente iguais
//Ex:
var1 !== 3

